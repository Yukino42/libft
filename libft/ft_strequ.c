#include "libft.h"

int		ft_strequ(char const *s1, char const *s2)
{
	int		n;

	n = 0;
	while ((s1[n] != '\0') & (s2[n] != '\0'))
	{
		if (s1[n] != s2[n])
			return (0);
		n++;
	}
	if (s1[n] != s2[n])
		return (0);
	return (1);
}
