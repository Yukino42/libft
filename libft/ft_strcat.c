char	*ft_strcat(char *s1, const char *s2)
{
	int		n;
	int		nb;

	n = 0;
	nb = 0;
	while (s1[n] != '\0')
		n++;
	while (s2[nb] != '\0')
	{
		s1[n] = s2[nb];
		n++;
		nb++;
	}
	s1[n] = '\0';
	return (s1);
}
