#include "libft.h"

void	ft_putnbr(int n)
{
	char				c;
	long				i;

	i = 1;
	if (n < 0)
		write(1, "-", 1);
	if (n == 0)
		write(1, "0", 1);
	while (n / i != 0)
		i = i * 10;
	while (i > 1)
	{
		if (n < 0)
		{
			c = (char)(-(((n % i) - (n % (i / 10))) / (i / 10)) + 48);
			write(1, &c, 1);
			n = -n;
		}
		else
		{
			c = (char)((((n % i) - (n % (i / 10))) / (i / 10)) + 48);
			write(1, &c, 1);
		}
		i = i / 10;
	}
}
